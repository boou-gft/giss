package br.com.bradesco.web.giss.view.bean.model.listaGenerica;


public class ListaJustificativaApuracaoVO {
	
	private String dataAtualizacao;
	private String sinalJustificativa;
	private String valorJustificativa;
	private String descJustificativa;
	private String codUsuarioManut;
	
	public String getDataAtualizacao() {
		return dataAtualizacao;
	}
	public void setDataAtualizacao(String dataAtualizacao) {
		this.dataAtualizacao = dataAtualizacao;
	}
	public String getSinalJustificativa() {
		return sinalJustificativa;
	}
	public void setSinalJustificativa(String sinalJustificativa) {
		this.sinalJustificativa = sinalJustificativa;
	}
	public String getValorJustificativa() {
		return valorJustificativa;
	}
	public void setValorJustificativa(String valorJustificativa) {
		this.valorJustificativa = valorJustificativa;
	}
	public String getDescJustificativa() {
		return descJustificativa;
	}
	public void setDescJustificativa(String descJustificativa) {
		this.descJustificativa = descJustificativa;
	}
	public String getCodUsuarioManut() {
		return codUsuarioManut;
	}
	public void setCodUsuarioManut(String codUsuarioManut) {
		this.codUsuarioManut = codUsuarioManut;
	}
	
}